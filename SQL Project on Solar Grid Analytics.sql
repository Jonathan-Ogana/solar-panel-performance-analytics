--- 14. Calculate moving average energy output over 7 days

SELECT 
	panel_id,
	[model_name],
	last_maintenance_date,
	AVG(energy_output_kwh) OVER (ORDER BY last_maintenance_date ROWS BETWEEN 6 PRECEDING AND CURRENT ROW) MovingAvg_7Days
FROM SolarPanelMetrics

-- 15. Compute rolling average efficiency ratings

SELECT
	panel_id,
	[model_name],
	last_maintenance_date,
	efficiency_rating,
	AVG(efficiency_rating) OVER (ORDER BY last_maintenance_date ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) RollingAvg
FROM SolarPanelMetrics

-- 16. Analyse 3-month rolling maintenance trends

SELECT 
    a.panel_id,
    a.model_name,
    a.last_maintenance_date,
    a.energy_output_kwh,
    -- Counts how many maintenance events occurred for this panel in the last 3 months
    (
        SELECT COUNT(*) 
        FROM SolarPanelMetrics b
        WHERE b.panel_id = a.panel_id
          AND b.last_maintenance_date BETWEEN DATEADD(month, -3, a.last_maintenance_date) AND a.last_maintenance_date
    ) AS Maintenance_Count_Past_3Months,
    
    -- Calculates the average energy output of the panel over that 3-month maintenance window
    (
        SELECT AVG(b.energy_output_kwh) 
        FROM SolarPanelMetrics b
        WHERE b.panel_id = a.panel_id
          AND b.last_maintenance_date BETWEEN DATEADD(month, -3, a.last_maintenance_date) AND a.last_maintenance_date
    ) AS Avg_Energy_Past_3Months
FROM SolarPanelMetrics a
ORDER BY a.panel_id, a.last_maintenance_date;

-- 17. Find panels performing above the moving average

SELECT
	panel_id,
	[model_name],
	energy_output_kwh,
	efficiency_rating
FROM(
	SELECT
		panel_id,
		[model_name],
		last_maintenance_date,
		energy_output_kwh,
		efficiency_rating,
		AVG(energy_output_kwh) OVER (ORDER BY [last_maintenance_date] ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW) MovingAvg
	FROM SolarPanelMetrics
)t WHERE energy_output_kwh > MovingAvg

-- 18. Detect sudden spikes or drops in energy production

SELECT
	panel_id,
	[model_name],
	energy_output_kwh,
	efficiency_rating
FROM(
	SELECT
		panel_id,
		[model_name],
		energy_output_kwh,
		efficiency_rating,
		LEAD(energy_output_kwh) OVER(PARTITION BY [model_name] ORDER BY last_maintenance_date) Spike
	FROM SolarPanelMetrics
)t WHERE energy_output_kwh < Spike 


