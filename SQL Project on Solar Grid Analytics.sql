-- 10. Detect duplicate maintenance activities using row numbering

SELECT *
FROM (
	SELECT
		panel_id,
		[model_name],
		last_maintenance_date,
		ROW_NUMBER() OVER (PARTITION BY panel_id, [model_name], last_maintenance_date ORDER BY last_maintenance_date) RowNumbering
	FROM SolarPanelMetrics
)t WHERE RowNumbering > 1

-- 11. Assign performance tiers using NTILE()

SELECT
	panel_id,
	[model_name],
	energy_output_kwh,
	NTILE(3) OVER (ORDER BY energy_output_kwh DESC) PerformanceQuartile,
	CASE
		WHEN NTILE(4) OVER (ORDER BY energy_output_kwh DESC) = 1 THEN 'High'
		WHEN NTILE(4) OVER (ORDER BY energy_output_kwh DESC) = 2 THEN 'Average'
		WHEN NTILE(4) OVER (ORDER BY energy_output_kwh DESC) = 3 THEN 'Low'
	END PerformanceTiers
FROM SolarPanelMetrics

-- 12. Rank panels within each model group by efficiency

SELECT
	panel_id,
	[model_name],
	efficiency_rating,
	DENSE_RANK() OVER (PARTITION BY [model_name] ORDER BY efficiency_rating DESC) RankedPanels
FROM SolarPanelMetrics

-- 13. Find the second-highest energy-producing panel in each category

SELECT * 
